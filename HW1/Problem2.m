%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Part A
% x_1[n] = -1.9sin(.1\pi n - \pi / 6) for -10<=n<=10
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

n = [-20:20];
x1 = -1.9 * sin(.1*pi*n-pi/6);

subplot(2, 2, 1);
stem(n, x1, 'filled');
grid on;
xlabel('n');
ylabel('-1.9sin(0.1\pi n - \pi/6');
title('a');

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Part B
% x_2[n] = 2*1.1^{1.9n} - 2.1*.9^{.7n} for -10<=n<=10
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
n = [-10:10];
x2 = 2*1.1.^(1.9*n)-2.1*0.9.^(.7*n);

subplot(2, 2, 2);
stem(n, x2, 'filled');
grid on;
xlabel('n');
ylabel('2*(1.1)^{-1.9n} - 2.1(.9)^{.7n}');
title('(b)');

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Part C
% x_2[n] = (-1.2)^ne^{j\pi n} 0<=n<=50 plot real and imag. parts.
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
n = [0:50];
x3 = (-1.2).^(n).*exp(j*pi*n);

subplot(2, 2, 3);
stem(n, real(x3), 'filled');
grid on;
xlabel('n');
ylabel('(-1.2)^n*e^(j \pi n');
title('(c) - Real Part');

subplot(2, 2, 4);
stem(n, imag(x3), 'filled');
grid on;
xlabel('n');
ylabel('(-1.2)^n*e^(j \pi n');
title('(c) - Real Part');
